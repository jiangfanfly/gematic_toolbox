% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%求解回归方程
x1=handles.x1;
x2=handles.x2;
y=handles.y;
xh=handles.xh;
n=size(xh);
n=n(1);
B=[ones(n,1),x1,x2];          %构建B阵
N=B'*B
a=pinv(N)*B'*y;               %求解回归方程系数
subplot(handles.axes1);       
v=B*a-y;
yy=B*a;
x11=linspace(max(x1),min(x1),20);
x22=linspace(min(x2),max(x2),20);
yy1=[ones(20,1),x11',x22']*a;
plot3(x11,x22,yy1,'-r');      %绘制回归方程直线图
legend('离散点','回归方程');
hold on;
sigma=sqrt(v'*v/(n-(2+1)));
set(handles.edit1,'string',num2str(a(1)));
set(handles.edit2,'string',num2str(a(2)));
set(handles.edit3,'string',num2str(a(3)));
set(handles.edit4,'string',num2str(sigma));
handles.B=B;
handles.a=a;
handles.yy=yy;
handles.sigma=sigma;
handles.n=n;
handles.N=N;
guidata(hObject,handles)

