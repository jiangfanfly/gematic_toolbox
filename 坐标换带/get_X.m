function [X]=get_X(B,a,b,e2)
%计算子午线弧长
%输入纬度B,长半轴a,短半轴b,第二偏心率e2,输出子午线弧长x
b0=1-3.*e2^2./4+45.*e2^4./64-175.*e2^6./256+11025.*e2^8./16384;
b2=b0-1;
b4=15.*e2^4./32-175.*e2^6./384+3675.*e2^8./8192;
b6=-35.*e2^6./96+735.*e2^8./2048;
b8=315.*e2^8./1024;
c=a.^2./b;
X=c.*(b0.*B+(b2.*cos(B)+b4.*cos(B).^3+b6.*cos(B).^5+b8.*cos(B).^7).*sin(B));
end