sy=[x2',y2'];
fid=fopen('坐标换带结果.txt','wt');
[m,n]=size(sy);
for i=1:1:m
   for j=1:1:n
      if j==n
      fprintf(fid,'%f\n',sy(i,j));
      else
      fprintf(fid,'%f\t',sy(i,j));
      end
   end
end
